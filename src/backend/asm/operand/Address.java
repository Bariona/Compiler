package backend.asm.operand;

public class Address extends Immediate {
  public String name;

  public Address(Boolean isHi, String name) {
    super(isHi ? 1 : -1);
    this.name = name;
  }

  @Override
  public String toString() {
    return "%" + (this.value > 0 ? "hi" : "lo") + "(" + this.name+ ")";
  }
}
